package com.terangalink.backend.enums;


/**
 * Types de logements disponibles sur la plateforme TerangaLink.

 * STUDIO : logement individuel composé d'une seule pièce principale.
 * ROOM : chambre privée chez un particulier ou dans un logement partagé.
 * SHARED_FLAT : colocation avec plusieurs occupants.
 * APARTMENT : appartement complet.
 * HOUSE : maison individuelle.
 */
public enum HousingType {
    STUDIO,
    ROOM,
    SHARED_FLAT,
    APARTMENT,
    HOUSE
}